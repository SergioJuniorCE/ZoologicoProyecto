    private void cuidadorRegistrarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cuidadorRegistrarBotonActionPerformed
        String especieCuidada = (String) especieCuidadaCombobox.getSelectedItem();
        String empleado = (String) empleadoCombobox.getSelectedItem();
        LocalDateTime fecha = fechaCuidadoDatetimePicker.getDateTimeStrict();
        String especieExperto = (String) especieExpertoCombobox.getSelectedItem();
        String especieBasico = (String) especieBasicoCombobox.getSelectedItem();

        try {
            Control.createCuidador(especieCuidada, empleado, fecha, especieExperto, especieBasico);
            JOptionPane.showMessageDialog(null, "Cuidador Creado");
        } catch (DAOException e) {
            JOptionPane.showMessageDialog(null, "Cuidador no creado.");
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_cuidadorRegistrarBotonActionPerformed

